package javaapplication2;

import java.util.Scanner;

public class DoWhileLoop {

    public static void main(String[] args) {

        int i = 12;
        System.out.println("Do while");
        do {
            System.out.println("Hello");

            i++;

        } while (i < 5);
        i = 12;
        System.out.println("While loop");
        while (i < 5) {
            System.out.println("Hello");

            i++;

        }

    }

}
